package lesson3;

public class Task7_4 {
    public static void main(String[] args){

        System.out.println("корень квадратный числа 144 = " + Math.sqrt(144));
        System.out.println("модуль числа -37 = " + Math.abs(-37));
        System.out.println("минимальное число из 10, 97 = " + Math.min(10, 97));
        System.out.println("максимальное число из 115, 72 = " + Math.max(115, 72));
        System.out.println("рандомное число = " + Math.random());
        System.out.println("корень кубический числа 125 = " + Math.cbrt(125));
        System.out.println("число 3 в степени 4 = " + Math.pow(3, 4));
        System.out.println("преобразует угол в 60 градусов в радианы = " + Math.toRadians(60));
    }
}
